
/**
 * op_add - add operation
 *
 * @a: first number
 * @b: second number
 * Return: returns the sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substraction operation
 *
 * @a: first number
 * @b: second number
 * Return: returns the result of substracting a from b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplicaton operation
 *
 * @a: first number
 * @b: second number
 * Return: returns the result of the multiplication of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - division operation
 *
 * @a: first number
 * @b: second number
 * Return: returns the result of the divison of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - moduli operation
 *
 * @a: first number
 * @b: second number
 * Return: returns the rest of a / b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
